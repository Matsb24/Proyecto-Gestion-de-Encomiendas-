package ClasesModeloDTO;


public class EmpresasDTO {
    
protected int Empresa_ID;
protected String Nombre_Empresa;	

    /**
     * @return the Empresa_ID
     */
    public int getEmpresa_ID() {
        return Empresa_ID;
    }

    /**
     * @param Empresa_ID the Empresa_ID to set
     */
    public void setEmpresa_ID(int Empresa_ID) {
        this.Empresa_ID = Empresa_ID;
    }

    /**
     * @return the Nombre_Empresa
     */
    public String getNombre_Empresa() {
        return Nombre_Empresa;
    }

    /**
     * @param Nombre_Empresa the Nombre_Empresa to set
     */
    public void setNombre_Empresa(String Nombre_Empresa) {
        this.Nombre_Empresa = Nombre_Empresa;
    }
}

