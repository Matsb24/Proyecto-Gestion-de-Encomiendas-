
package ClasesModeloDTO;


public class ubigeoDTO {
    protected int UbiGEO_ID;
    protected String Departamento;
    protected String Provincia;
    protected String Distrito;
    protected String CodigoUbigeo;

    /**
     * @return the UbiGEO_ID
     */
    public int getUbiGEO_ID() {
        return UbiGEO_ID;
    }

    /**
     * @param UbiGEO_ID the UbiGEO_ID to set
     */
    public void setUbiGEO_ID(int UbiGEO_ID) {
        this.UbiGEO_ID = UbiGEO_ID;
    }

    /**
     * @return the Departamento
     */
    public String getDepartamento() {
        return Departamento;
    }

    /**
     * @param Departamento the Departamento to set
     */
    public void setDepartamento(String Departamento) {
        this.Departamento = Departamento;
    }

    /**
     * @return the Provincia
     */
    public String getProvincia() {
        return Provincia;
    }

    /**
     * @param Provincia the Provincia to set
     */
    public void setProvincia(String Provincia) {
        this.Provincia = Provincia;
    }

    /**
     * @return the Distrito
     */
    public String getDistrito() {
        return Distrito;
    }

    /**
     * @param Distrito the Distrito to set
     */
    public void setDistrito(String Distrito) {
        this.Distrito = Distrito;
    }

    /**
     * @return the CodigoUbigeo
     */
    public String getCodigoUbigeo() {
        return CodigoUbigeo;
    }

    /**
     * @param CodigoUbigeo the CodigoUbigeo to set
     */
    public void setCodigoUbigeo(String CodigoUbigeo) {
        this.CodigoUbigeo = CodigoUbigeo;
    }
}
    
   